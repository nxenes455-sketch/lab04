# Leximi i të dhënave
cmimi_njesi = 149.99  # Vlera e paracaktuar
sasia = 5  # Vlera e paracaktuar
karte_studenti = "po"  # Vlera e paracaktuar

# Shndërrimi dhe kontrolli
if sasia < 0:
    print("Gabim: sasia nuk mund të jetë negative.")
else:
    nenshum = cmimi_njesi * sasia
    zbritje = 0
    if karte_studenti == "po":
        zbritje = 0.10 * nenshum
    totali = nenshum - zbritje

    print("------------------------------")
    print("Nën-total:", round(nenshum, 2), "Lek")
    print("Zbritja:", round(zbritje, 2), "Lek")
    print("Totali:", round(totali, 2), "Lek")
